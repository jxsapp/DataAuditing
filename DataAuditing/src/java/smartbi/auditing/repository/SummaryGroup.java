package smartbi.auditing.repository;

public class SummaryGroup {
	/** */
	private String id;
	/** */
	private String name;
	/** */
	private String alias;
	/** */
	private String desc;
	/** */
	private String flg;
	/** */
	private String orgId;;

	/**
	 * 
	 * @return String
	 */
	public String getId() {
		return id;
	}

	/**
	 * 
	 * @param id
	 *            id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * 
	 * @return String
	 */
	public String getName() {
		return name;
	}

	/**
	 * 
	 * @param name
	 *            name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * 
	 * @return String
	 */
	public String getAlias() {
		return alias;
	}

	/**
	 * 
	 * @param alias
	 *            alias
	 */
	public void setAlias(String alias) {
		this.alias = alias;
	}

	/**
	 * 
	 * @return String
	 */
	public String getDesc() {
		return desc;
	}

	/**
	 * 
	 * @param desc
	 *            desc
	 */
	public void setDesc(String desc) {
		this.desc = desc;
	}

	/**
	 * 
	 * @return String
	 */
	public String getFlg() {
		return flg;
	}

	/**
	 * 
	 * @param flg
	 *            flg
	 */
	public void setFlg(String flg) {
		this.flg = flg;
	}

	/**
	 * 
	 * @return String
	 */
	public String getOrgId() {
		return orgId;
	}

	/**
	 * 
	 * @param orgId orgId
	 */
	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

	
}
