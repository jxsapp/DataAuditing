public class Assignee {
	/** */
	private String type;
	/** */
	private String value;

	/**
	 * 
	 * @return String
	 */
	public String getType() {
		return type;
	}

	/**
	 * 
	 * @param type
	 *            type
	 */
	public void setType(String type) {
		this.type = type;
	}

	/**
	 * 
	 * @return String
	 */
	public String getValue() {
		return value;
	}

	/**
	 * 
	 * @param value
	 *            value
	 */
	public void setValue(String value) {
		this.value = value;
	}

}
